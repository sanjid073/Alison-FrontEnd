function viewMore() {
  document.getElementById("overflow-list").style.display = "block";
  document.getElementById("view-more-button-1").style.display = "block";
  document.getElementById("view-more-button").style.display = "none";
}
function viewMoreX() {
  document.getElementById("overflow-list").style.display = "none";
  document.getElementById("view-more-button-1").style.display = "none";
  document.getElementById("view-more-button").style.display = "block";
}
function readMore() {
  document.getElementById("banner-text-1").style.display = "block";
  document.getElementById("read-btn").style.display = "none";
}
